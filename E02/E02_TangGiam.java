public class E02_TangGiam {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x++);// 5 -> x = 6
        System.out.println(--x);// 5 -> x = 5
        System.out.println(x--);// 5 -> x = 4
        System.out.println(x++);// 4 -> x = 5
        System.out.println(++x);// 6 -> x = 6
        
    }
}
